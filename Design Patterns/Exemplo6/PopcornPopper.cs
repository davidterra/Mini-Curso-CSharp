using System;

namespace Exemplo6
{
    public class PopcornPopper
    {
        internal void On()
        {
            System.Console.WriteLine("Popcorn Popper on");
        }

        internal void Pop()
        {
            System.Console.WriteLine("Popcorn Popping popcorn");
        }

        internal void Off()
        {
            System.Console.WriteLine("Popcorn Popper Off.");
        }
    }
}